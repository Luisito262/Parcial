package com.mycompany.parcial;

public class Vendedor {

    String id;
    String nombre;
    String apellidos;
    String direccion;
    String telefono;
    String celular;

    public Vendedor(String id, String nombre, String apellidos, String direccion, String telefono, String celular) {
        this.id = id;
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.direccion = direccion;
        this.telefono = telefono;
        this.celular = celular;
    }

}
